async function createWelcomeDiscussion() {
  console.log('üëã Creating welcome discussion...');
  
  try {
    // First, get discussion categories using GraphQL
    const categoriesQuery = `
      query GetCategories($owner: String!, $repo: String!) {
        repository(owner: $owner, name: $repo) {
          discussionCategories(first: 10) {
            nodes {
              id
              name
              emoji
              description
            }
          }
        }
      }
    `;
    
    const categoriesData = await makeGraphQLRequest(categoriesQuery, {
      owner: REPO_OWNER,
      repo: REPO_NAME
    });
    
    const categories = categoriesData.repository.discussionCategories.nodes;
    console.log(`üìÇ Found ${categories.length} discussion categories`);
    
    // Find a suitable category (prefer Announcements, General, or the first available)
    const preferredCategories = ['Announcements', 'General', 'Announcement'];
    let selectedCategory = categories.find(cat => 
      preferredCategories.some(preferred => 
        cat.name.toLowerCase().includes(preferred.toLowerCase())
      )
    );
    
    // If no preferred category found, use the first available
    if (!selectedCategory && categories.length > 0) {
      selectedCategory = categories[0];
    }
    
    if (!selectedCategory) {
      console.log('‚ö†Ô∏è  No discussion categories found. Please create categories manually in your repository settings.');
      console.log('üí° Go to your repository ‚Üí Settings ‚Üí scroll to "Features" ‚Üí Discussions ‚Üí "Set up discussions"');
      return;
    }
    
    console.log(`üìÇ Using category: "${selectedCategory.name}" (${selectedCategory.emoji || 'üí¨'})`);

    // Create the welcome discussion using GraphQL
    const accountTypeText = ownerType === 'Organization' ? 'organization' : 'project';
    const welcomeBody = `# Welcome to our Support Portal! üéâ

This community support portal has been automatically set up for our ${accountTypeText} to help you get answers to your questions and connect with other users.

## How to get help:
- üîç **Search first**: Check if your question has already been answered
- ‚ùì **Ask questions** in the Q&A category  
- üí° **Share ideas** for new features or improvements
- üôå **Show off** what you've built with our project

## Guidelines:
- Be respectful and helpful to other community members
- Provide clear details when asking questions
- Use appropriate categories for your discussions
- Search before posting to avoid duplicates

${ownerType === 'Organization' ? 
  'This portal serves our entire organization and all community members are welcome!' : 
  'Thanks for checking out my project! Feel free to ask questions or share feedback.'
}

---
*This portal was automatically configured using Doorway - a zero-cost help desk solution powered by GitHub Discussions.*`;

    const createDiscussionMutation = `
      mutation CreateDiscussion($repositoryId: ID!, $categoryId: ID!, $title: String!, $body: String!) {
        createDiscussion(input: {
          repositoryId: $repositoryId
          categoryId: $categoryId
          title: $title
          body: $body
        }) {
          discussion {
            id
            number
            title
            url
          }
        }
      }
    `;

    // Get repository ID for the mutation
    const repoQuery = `
      query GetRepository($owner: String!, $repo: String!) {
        repository(owner: $owner, name: $repo) {
          id
        }
      }
    `;
    
    const repoData = await makeGraphQLRequest(repoQuery, {
      owner: REPO_OWNER,
      repo: REPO_NAME
    });

    const result = await makeGraphQLRequest(createDiscussionMutation, {
      repositoryId: repoData.repository.id,
      categoryId: selectedCategory.id,
      title: 'Welcome to our Support Portal!',
      body: welcomeBody
    });
    
    console.log('‚úÖ Created welcome discussion with account-specific content');
    console.log(`üîó Discussion #${result.createDiscussion.discussion.number}: ${result.createDiscussion.discussion.url}`);
    
  } catch (error) {
    if (error.message.includes('already exists') || error.message.includes('422')) {
      console.log('‚ö†Ô∏è  Welcome discussion may already exist or there was a validation error');
    } else {
      console.log('‚ö†Ô∏è  Could not create welcome discussion:', error.message);
      console.log('üí° You can create a welcome discussion manually in your repository');
    }
  }
}

